package com.lcq.tftgdb.pojo;

public class Categories {
    private String name;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "categories{" +
                "name='" + name + '\'' +
                '}';
    }
}
